class TechnicalKnowledgeLoader:
    """
    Загрузчик технических знаний для ИИ
    Позволяет наполнять мозг специализированными знаниями
    по техническим дисциплинам
    """

    def __init__(self, brain):
        """
        Инициализация загрузчика
        Args:
            brain: экземпляр Cortex (мозг)
        """
        self.brain = brain
        self.categories = {
            'mathematics': self.get_mathematics_knowledge(),
            'geodesy': self.get_geodesy_knowledge(),
            'cartography': self.get_cartography_knowledge(),
            'construction': self.get_construction_knowledge(),
            'automotive': self.get_automotive_knowledge(),
            'physics': self.get_physics_knowledge(),
            'programming': self.get_programming_knowledge()
        }
        print("📚 TechnicalKnowledgeLoader инициализирован")
        print(f"   Категорий: {len(self.categories)}")

    def load_all(self, depth: str = 'basic') -> Dict:
        """
        Загрузить все знания
        Args:
            depth: 'basic', 'medium', 'deep'
        Returns:
            Dict: статистика загрузки
        """
        stats = {
            'total_neurons': 0,
            'by_category': {},
            'synapses_created': 0
        }

        for category, knowledge_list in self.categories.items():
            print(f"\n📖 Загрузка категории: {category.upper()}")

            # Фильтруем по глубине
            if depth == 'basic':
                knowledge_list = knowledge_list[:20]
            elif depth == 'medium':
                knowledge_list = knowledge_list[:50]
            # deep = все знания

            category_count = 0
            for item in knowledge_list:
                self.brain.add_knowledge(
                    f"{category}_{item['id']}",
                    item['content'],
                    category,
                    item.get('related', [])
                )
                category_count += 1

            stats['by_category'][category] = category_count
            stats['total_neurons'] += category_count

        # Получаем статистику синапсов
        stats['synapses_created'] = self.brain.graph.number_of_edges()

        return stats

    def load_category(self, category: str, depth: str = 'basic') -> Dict:
        """
        Загрузить знания конкретной категории
        Args:
            category: название категории
            depth: глубина загрузки
        Returns:
            Dict: статистика
        """
        if category not in self.categories:
            return {'error': f'Категория {category} не найдена'}

        stats = {
            'category': category,
            'neurons_created': 0
        }

        knowledge_list = self.categories[category]

        if depth == 'basic':
            knowledge_list = knowledge_list[:20]
        elif depth == 'medium':
            knowledge_list = knowledge_list[:50]

        for item in knowledge_list:
            self.brain.add_knowledge(
                f"{category}_{item['id']}",
                item['content'],
                category,
                item.get('related', [])
            )
            stats['neurons_created'] += 1

        return stats

